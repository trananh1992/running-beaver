package android.runningbeaver.objects;

public final class Position {

	private final float x;
	private final float y;

	public Position(float d, float e) {
		super();
		this.x = d;
		this.y = e;
	}

	public float getX() {
		return x;
	}

	public float getY() {
		return y;
	}

}
